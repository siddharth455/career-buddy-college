                            <h3>course description</h3>
                            <p>The Master of Physiotherapy (MPT) is a comprehensive two-year postgraduate degree program aimed at developing advanced clinical knowledge and practical skills in the field of physical therapy. This specialized course is ideal for students and professionals who wish to deepen their understanding of human movement science and gain expertise in diagnosing and managing a wide range of physical impairments and functional limitations.</p>
                            <p>Physiotherapy plays a pivotal role in rehabilitative health care by promoting mobility, functional ability, and quality of life through evidence-based therapeutic interventions. The MPT curriculum integrates advanced theoretical knowledge with intensive hands-on clinical training, allowing students to master various techniques such as manual therapy, electrotherapy, therapeutic exercise, and neurodevelopmental treatments.</p>
                            <p>Throughout the program, students are introduced to diverse specialties including Orthopedic Physiotherapy, Neurological Physiotherapy, Cardiopulmonary Physiotherapy, Sports Physiotherapy, Pediatrics, and Geriatrics, enabling them to choose a focused area of practice based on their career interests. In addition to clinical proficiency, the course also emphasizes research methodology, biomechanics, health promotion, and patient education— equipping graduates to work in multidisciplinary teams across hospitals, rehabilitation centers, sports organizations, academic institutions, and private clinics. Many also pursue roles in teaching, research, or even independent practice.</p>
                            <p>By the end of the program, graduates are prepared to assess, plan, and execute individualized treatment strategies for patients of all ages and health conditions, contributing meaningfully to their recovery, mobility, and overall well-being</p>
